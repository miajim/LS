# What is exception handling and what problem does it solve? 


# Exception handling prevents code from stopping and providing an error message. 
# It anticipates an input that could provide an error and outlines 
# (a) step(s) if an error does occur, so that the program can finish executing. 

# LS Solution
# Exception han dling is a structure used to handle the possibility of an error
# occuring in a program. It is a way of handling the error by changing the flow 
